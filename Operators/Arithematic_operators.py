# Examples of Arithmetic Operator
a = 9
b = 4
  
# Addition of numbers
add = a + b
print(add)
  
# Subtraction of numbers
sub = a - b
print(sub)

# Multiplication of number
mul = a * b
print(mul)

# Division(float) of number
div1 = a / b
print(div1)

# Division(floor) of number
div2 = a // b
print(div2)

# Modulous of both number
mod = a % b
print(mod)

