#to demonstrate lambda functions

#Lamda functions are anonymous functions which do not have a name and consist of only one expression

f = lambda  a: a*a                                     # func_name = lambda variables: return values
result=f(5)  #here f is the function
print(result)

f = lambda  a,b: a+b    
result=f(5,6)
print(result)

